package com.muhammet;

public class Runner_ForLoop {
    public static void main(String[] args) {
        /**
         * 1 ile 10 arasındaki sayıları ekran yazdıran kodu yazınız.
         * 1 ile 20 arasındaki sayıların toplamını bulun
         * 1 ile 100 arasındaki tek sayıları bulan kodu yazınız.  1,3,5,7,9,11,13....
         */

        for(int i=1; i<=100; i+=2){
            System.out.println(i);
        }

    }
}
